firstName = "John"
lastName = "Smith"
x = 5
y = 10

print("Variables in Python do not require a type, as it will automatically inherit one based on context. Some variables may need to be typecasted before an operation.\n\nExample:\n")
print("firstName:", firstName)
print("lastName:", lastName)
print("firstName + lastName = ", firstName + lastName)
print("\nAssignment operators should be used with data operations to perform an operation AND set the value of the variable.")
print("\nExample:\n")
print("x:", x)
print("y:", y)
print("x + y =", x + y)
print("x:", x)
print("y:", y)
print("Neither variable was changed with simple addition.\n")
x += y
print("x += y =", x)
print("x:", x)
print("y:", y)
print("\'x\' has now been changed to the value of 15 as it operated then saved the variable by using \'+=\'.")

